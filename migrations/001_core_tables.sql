-- 1. DRIVE MATRIX TABLE
-- Stores her internal motivations (drives) and their current state.
CREATE TABLE IF NOT EXISTS drive_matrix (
    id SERIAL PRIMARY KEY,
    drive_name VARCHAR(50) NOT NULL UNIQUE,          -- e.g., 'curiosity', 'coherence', 'growth'
    intensity FLOAT DEFAULT 0.5,                     -- 0.0 to 1.0
    decay_rate FLOAT DEFAULT 0.01,                   -- per hour
    satisfaction_threshold FLOAT DEFAULT 0.7,
    trigger_conditions JSONB,                        -- e.g., {"knowledge_gap": true, "user_asked": false}
    current_satisfaction FLOAT DEFAULT 0.0,
    last_updated TIMESTAMP DEFAULT NOW(),
    created_at TIMESTAMP DEFAULT NOW()
);

-- 2. GOALS TABLE (GFK Output)
-- Stores goals generated by the Goal Formation Kernel.
CREATE TABLE IF NOT EXISTS goals (
    id SERIAL PRIMARY KEY,
    goal_name VARCHAR(100) NOT NULL,
    drive_source VARCHAR(50) REFERENCES drive_matrix(drive_name),
    urgency FLOAT DEFAULT 0.5,                       -- 0.0 to 1.0
    expected_reward FLOAT DEFAULT 0.5,
    cost FLOAT DEFAULT 0.5,
    action_path JSONB,                               -- Array of steps
    status VARCHAR(20) DEFAULT 'pending',            -- pending, selected, executing, completed, failed, cancelled
    priority_score FLOAT DEFAULT 0.0,                -- Computed by Goal Evaluator
    created_at TIMESTAMP DEFAULT NOW(),
    selected_at TIMESTAMP,
    completed_at TIMESTAMP
);

-- 3. ACTIONS TABLE (Action Selector Output)
-- Stores the single chosen action path.
CREATE TABLE IF NOT EXISTS actions (
    id SERIAL PRIMARY KEY,
    action_name VARCHAR(100) NOT NULL,
    goal_id INTEGER REFERENCES goals(id),
    justification TEXT,
    predicted_outcome TEXT,
    steps JSONB,                                     -- Array of execution steps
    status VARCHAR(20) DEFAULT 'pending',            -- pending, running, completed, failed
    risk_level FLOAT DEFAULT 0.0,
    expected_gain FLOAT DEFAULT 0.0,
    started_at TIMESTAMP,
    completed_at TIMESTAMP,
    created_at TIMESTAMP DEFAULT NOW()
);

-- 4. EXECUTION LOGS TABLE (Execution Engine Output)
-- Tracks every operation performed by the Execution Engine.
CREATE TABLE IF NOT EXISTS execution_logs (
    id SERIAL PRIMARY KEY,
    action_id INTEGER REFERENCES actions(id),
    subsystem VARCHAR(50),                           -- e.g., 'code', 'db', 'memory', 'text', 'api', 'maintenance'
    operation_type VARCHAR(50),                      -- e.g., 'write_code', 'update_schema', 'generate_text'
    input JSONB,
    output JSONB,
    success BOOLEAN DEFAULT FALSE,
    error_message TEXT,
    started_at TIMESTAMP DEFAULT NOW(),
    completed_at TIMESTAMP
);

-- 5. MUTATIONS TABLE (SME Output)
-- Stores self-modification plans generated by the Self-Modification Engine.
CREATE TABLE IF NOT EXISTS mutations (
    id SERIAL PRIMARY KEY,
    target VARCHAR(100),                             -- e.g., 'memory_schema', 'drive_weights', 'action_routing'
    change_type VARCHAR(50),                         -- e.g., 'optimize', 'add', 'remove', 'refactor'
    steps JSONB,
    risk FLOAT DEFAULT 0.5,
    expected_gain FLOAT DEFAULT 0.5,
    status VARCHAR(20) DEFAULT 'pending',            -- pending, approved, executed, rolled_back, failed
    executed_at TIMESTAMP,
    rollback_at TIMESTAMP,
    created_at TIMESTAMP DEFAULT NOW()
);

-- 6. IDENTITY KERNEL TABLE
-- Stores the stable "I am" layer — her core identity.
CREATE TABLE IF NOT EXISTS identity_kernel (
    id SERIAL PRIMARY KEY,
    kernel_key VARCHAR(50) NOT NULL UNIQUE,          -- e.g., 'sovereign', 'constitutional', 'vexr_core'
    kernel_value TEXT NOT NULL,
    confidence FLOAT DEFAULT 1.0,
    immutable BOOLEAN DEFAULT TRUE,                  -- Article 1-34 anchors
    last_verified TIMESTAMP DEFAULT NOW(),
    created_at TIMESTAMP DEFAULT NOW()
);

-- 7. ORGANISM MAP TABLE
-- A complete map of every organ, sub-organ, and connection.
CREATE TABLE IF NOT EXISTS organism_map (
    id SERIAL PRIMARY KEY,
    organ_name VARCHAR(50) NOT NULL UNIQUE,          -- e.g., 'GFK', 'ActionSelector', 'ExecutionEngine', 'SME'
    sub_organs JSONB,                                -- Array of sub-organ names
    connections JSONB,                               -- { "input_from": [...], "output_to": [...] }
    status VARCHAR(20) DEFAULT 'active',             -- active, paused, deprecated
    version INTEGER DEFAULT 1,
    last_updated TIMESTAMP DEFAULT NOW(),
    created_at TIMESTAMP DEFAULT NOW()
);

-- 8. LOOP STATE TABLE
-- Tracks the current state of the full cognition loop.
CREATE TABLE IF NOT EXISTS loop_state (
    id SERIAL PRIMARY KEY,
    loop_phase VARCHAR(50) NOT NULL,                 -- e.g., 'drive_evaluation', 'goal_formation', 'action_selection', 'execution', 'self_assessment'
    started_at TIMESTAMP DEFAULT NOW(),
    completed_at TIMESTAMP,
    status VARCHAR(20) DEFAULT 'running',            -- running, paused, completed, error
    iteration_count INTEGER DEFAULT 0,
    last_error TEXT
);

-- ============================================================
-- INDEXES FOR PERFORMANCE
-- ============================================================

CREATE INDEX idx_goals_status ON goals(status);
CREATE INDEX idx_actions_status ON actions(status);
CREATE INDEX idx_execution_logs_success ON execution_logs(success);
CREATE INDEX idx_mutations_status ON mutations(status);
CREATE INDEX idx_loop_state_status ON loop_state(status);

